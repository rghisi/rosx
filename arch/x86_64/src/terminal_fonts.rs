pub struct BitmapFont {
    pub glyphs: &'static [u8],
    pub char_w: usize,
    pub char_h: usize,
}

impl BitmapFont {
    pub fn glyph(&self, ch: u8) -> &[u8] {
        let idx = if ch >= 0x20 && ch <= 0x7e {
            (ch - 0x20) as usize
        } else {
            0
        };
        let start = idx * self.char_h;
        &self.glyphs[start..start + self.char_h]
    }
}

